import unittest 
from src.operations import *


class TestOperations(unittest.TestCase):
    def test_ROTR(self):
        self.assertEqual(ROTR('01101000011001010110110001101100', 4), '11000110100001100101011011000110')
     
    def test_MOD32(self):
        self.assertEqual(MOD32(['01101000011001010110110001101100', '01101000011001010110110001101100']), '11010000110010101101100011011000')

    def test_RSHIFT(self):
        self.assertEqual(RSHIFT('01101000011001010110110001101100', 4), '00000110100001100101011011000110')

    def test_AND(self):
        self.assertEqual(AND(['01101000011001010110110001101100', '01101000011001010110110001101100']), '01101000011001010110110001101100')

    def test_XOR(self):
        self.assertEqual(XOR(['01101000011001010110110001101100', '01101000011001010110110001101100']), '00000000000000000000000000000000')

    def test_NOT(self):
        self.assertEqual(NOT('0110100001100101011011000110110'), '-0110100001100101011011000110111')

if __name__ == '__main__':
    unittest.main()
   
