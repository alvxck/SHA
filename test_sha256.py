import unittest
from sha256 import SHA256

class TestSHA256(unittest.TestCase):
    def test_hash(self):
        test = SHA256('hello world')
        self.assertEqual(test.hash(), ['10111001010011010010011110111001', '10010011010011010011111000001000', '10100101001011100101001011010111', '11011010011111011010101111111010', '11000100100001001110111111100011', '01111010010100111000000011101110', '10010000100010001111011110101100', '11100010111011111100110111101001'])

if __name__ == '__main__':
    unittest.main()
    